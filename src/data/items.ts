export const ITEMS: ItemDefinition[] = ITEM_SEEDS.map((item) => createItemDefinition(item));

export function getItemsForBook(bookId: BookId) {
  if (bookId === "mixed") {
    return ITEMS;
  }

  return ITEMS.filter((item) => item.bookId === bookId);
}

export function getPictureBookById(bookId: BookId) {
  return PICTURE_BOOKS.find((book) => book.id === bookId) ?? null;
}

function createItemDefinition(item: ItemSeed): ItemDefinition {
  const speechName = `${item.article} ${item.label}`;
  const promptHint = getPromptHint(item.bookId, item.motionStyle);

  return {
    id: item.id,
    emoji: item.emoji,
    label: item.label,
    speechName,
    questionPrompt: `Wo ist ${speechName.toLowerCase()}?`,
    advancedPrompts:
      item.advancedPrompts ?? [
        `Wo ist ${speechName.toLowerCase()}?`,
        `Tippe auf ${speechName.toLowerCase()}.`,
        promptHint,
      ],
    phrase: `${speechName} ${item.phrase}`,
    color: item.color,
    bookId: item.bookId,
    motionStyle: item.motionStyle,
    soundEffect: item.soundEffect,
    imageSource: item.imageSource,
  };
}

function getPromptHint(bookId: PictureBookId, motionStyle: ItemMotionStyle) {
  const book = getPictureBookById(bookId);

  return (
    book?.promptHintsByMotionStyle?.[motionStyle] ??
    book?.defaultPromptHint ??
    DEFAULT_PROMPT_HINTS_BY_MOTION_STYLE[motionStyle]
  );
}
